namespace Microsoft.Xna.Framework.Input
{
    /// <summary>
    /// Identifies the state of a controller button. 
    /// </summary>
    public enum ButtonState
    {
        /// <summary>
        /// The button is pressed.
        /// </summary>
        Released,
        /// <summary>
        /// The button is released.
        /// </summary>
        Pressed
    }
}
