namespace Microsoft.Xna.Framework.Input
{
    /// <summary>
    /// Identifies the state of a keyboard key. 
    /// </summary>
    public enum KeyState
    {
        /// <summary>
        /// Key is pressed.
        /// </summary>
        Down = 1,
        /// <summary>
        /// Key is released.
        /// </summary>
        Up = 0
    }
}
